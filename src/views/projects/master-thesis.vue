        <p>My master’s thesis was written in collaboration with Novo Nordisk, where I worked with the internal Digital Checklist app used by factory operators in production. The tool was designed as a cognitive aid to reduce errors and support operators in complex workflows. The original design assumed that operators would wear a mobile phone on their wrist, complete one task at a time, and immediately return to the app to confirm it. In reality, many used it differently. Several operators described that wearing the phone was too heavy and hindered them in their work, and instead of checking off tasks one by one, they left the phone on a desk and completed multiple steps before returning to the app.</p>

